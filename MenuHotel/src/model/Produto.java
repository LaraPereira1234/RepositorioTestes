package model;

public class Produto {
	
	private int id;
	private String nome;
	private String descricao;
	private float preco;
	private String categoria;
	private String ingrediente;
	private String alergenicos;
	private int disponibilidade;
	private String acucar;
	private String gordura;
	private float calorias;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public float getPreco() {
		return preco;
	}
	public void setPreco(float preco) {
		this.preco = preco;
	}
	public String getCategoria() {
		return categoria;
	}
	public void setCategoria(String categoria) {
		this.categoria = categoria;
	}
	public String getIngrediente() {
		return ingrediente;
	}
	public void setIngrediente(String ingrediente) {
		this.ingrediente = ingrediente;
	}
	public String getAlergenicos() {
		return alergenicos;
	}
	public void setAlergenicos(String alergenicos) {
		this.alergenicos = alergenicos;
	}
	public int getDisponibilidade() {
		return disponibilidade;
	}
	public void setDisponibilidade(int disponibilidade) {
		this.disponibilidade = disponibilidade;
	}
	public String getAcucar() {
		return acucar;
	}
	public void setAcucar(String acucar) {
		this.acucar = acucar;
	}
	public String getGordura() {
		return gordura;
	}
	public void setGordura(String gordura) {
		this.gordura = gordura;
	}
	public float getCalorias() {
		return calorias;
	}
	public void setCalorias(float calorias) {
		this.calorias = calorias;
	}
	
	
	

}
